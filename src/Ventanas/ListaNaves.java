    private void txtBuscarKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarKeyTyped
        // TODO add your handling code here:
        txtBuscador=txtBuscar.getText();
        List<Nave> navs = Fabrica.getInstancia().getInterfazNave().getNavesByName(txtBuscador.toLowerCase(),naves);
        DefaultTableModel modeloE = (DefaultTableModel)jTableNaves.getModel();
        modeloE.setRowCount(0);
        for (int i = 0; i < navs.size(); i++) {
            Nave n=(Nave)navs.get(i);
            Object[] dat = {n,n.getNombre(),n.getPeso(),n.getCombustible(),n.getEstado(),n.getTipo()};
            modeloE.addRow(dat);
        }
    }//GEN-LAST:event_txtBuscarKeyTyped

