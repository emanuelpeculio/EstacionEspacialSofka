        public final void verNaves() {
        DefaultTableModel modelo = (DefaultTableModel) jTableNaves.getModel();
        modelo.setRowCount(0);
        naves = Fabrica.getInstancia().getInterfazNave().getAllNaves();
        for (int i = 0; i < naves.size(); i++) {
            Nave n=(Nave)naves.get(i);
            Object[] dat = {n,n.getNombre(),n.getPeso(),n.getCombustible(),n.getEstado(),n.getTipo()};
            modelo.addRow(dat);
        }
        DefaultTableCellRenderer Alinear = new DefaultTableCellRenderer();
        Alinear.setHorizontalAlignment(SwingConstants.CENTER);//.LEFT .RIGHT .CENTER
        jTableNaves.getColumnModel().getColumn(0).setCellRenderer(Alinear);
        jTableNaves.getColumnModel().getColumn(1).setCellRenderer(Alinear);
        jTableNaves.getColumnModel().getColumn(2).setCellRenderer(Alinear);
        jTableNaves.getColumnModel().getColumn(3).setCellRenderer(Alinear);
        jTableNaves.getColumnModel().getColumn(4).setCellRenderer(Alinear);
        jTableNaves.getColumnModel().getColumn(5).setCellRenderer(Alinear);
        
    }
