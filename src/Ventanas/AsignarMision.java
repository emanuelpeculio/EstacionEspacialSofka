public boolean verificarDatos(){
        if(txtCod.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Debe ingresar un código", "ERROR", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        else if(txtNom.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Debe ingresar un nombre de misión", "ERROR", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        else if(txtMision.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Debe ingresar una misión", "ERROR", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        else if(txtDescrip.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Debe ingresar una descripción", "ERROR", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        else if(cbo_Nave.getSelectedItem().equals("Seleccione")){
            JOptionPane.showMessageDialog(this, "Debe seleccionar una nave", "ERROR", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        else if(cbo_Planeta.getSelectedItem().equals("Seleccione")){
            JOptionPane.showMessageDialog(this, "Debe seleccionar un Planeta Destino. ", "ERROR", JOptionPane.ERROR_MESSAGE);
            return false;
        }
 
       
        return true;  
    
 }
