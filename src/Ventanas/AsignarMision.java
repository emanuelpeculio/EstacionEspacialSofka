    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        if(verificarDatos()){
        if(Fabrica.getInstancia().getInterfazMision().verificarMision(Integer.parseInt(txtCod.getText()))){
        javax.swing.JOptionPane.showMessageDialog(null, "Ya existe una misión con ese codigo!","ERROR!",0);
        }else{
        Nave naveSel=null;
        for (Nave n : Nave) {
            if(n.getNombre().equals(cbo_Nave.getSelectedItem().toString())){
                naveSel=n;
                break;
            }
        }
        PlanetaDestino PSel=null;
        for (PlanetaDestino p : Planeta) {
            if(p.getNombre().equals(cbo_Planeta.getSelectedItem().toString())){
                PSel=p;
                break;
            }
        }
        Mision M = new Mision();
        M.setCodigo(Integer.parseInt(txtCod.getText()));
        M.setNombre(txtNom.getText());
        M.setMision(txtMision.getText());
        M.setEstado("En curso");
        M.setDescripcion(txtDescrip.getText());
        naveSel.setEstado("Misión en curso");
        M.setNave(naveSel);
        M.setPlanetadestino(PSel);
        conexionBD.getInstance().persist(M);
        
        JOptionPane.showMessageDialog(this, "Se ha creado la mision correctamente", "ÉXITO", JOptionPane.INFORMATION_MESSAGE);
        //Limpiar cajas de texto ---------------------------------------
        txtCod.setText("");
        txtNom.setText("");
        txtMision.setText("");
        txtDescrip.setText("");
    }//GEN-LAST:event_btn_guardarActionPerformed
    }
    }
    public void cargarNaves(){
        List<Nave> Naves=Fabrica.getInstancia().getInterfazNave().getAllNaves();
        Nave=Naves;
        Naves.forEach((Nave Nv)->{
            cbo_Nave.addItem(Nv.getNombre());        
        });
    }
        
    public void cargarPlanetas(){
        List<PlanetaDestino> Planetas=Fabrica.getInstancia().getInterfazPlaneta().getAllPlanetas();
        Planeta=Planetas;
        Planetas.forEach((PlanetaDestino Pl)->{
            cbo_Planeta.addItem(Pl.getNombre());        
        });
    }
    
