 public boolean verificarDatos(){
        if(txtCod.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Debe ingresar un c√≥digo", "ERROR", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        else if(txtNom.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Debe ingresar un nombre de nave", "ERROR", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        else if(txtPeso.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Debe ingresar un peso", "ERROR", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        else if(txtComb.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Debe ingresar un combustible", "ERROR", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        else if(combo_tipo.getSelectedItem().equals("Seleccione")){
            JOptionPane.showMessageDialog(this, "Debe ingresar un Tipo de nave", "ERROR", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        else if(combo_tipo.getSelectedItem().equals("Lanzadera")){
            if(txtProp.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Debe ingresar una propulsi√≥n", "ERROR", JOptionPane.ERROR_MESSAGE);
            return false;
        }}
        else if(combo_tipo.getSelectedItem().equals("Tripulada")){
            if(txtOrb.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Debe ingresar una Orbita. EJ:1281299 km de la tierra", "ERROR", JOptionPane.ERROR_MESSAGE);
            return false;
        }
            if(txtCantPasj.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Debe ingresar un numero de pasajeros", "ERROR", JOptionPane.ERROR_MESSAGE);
            return false;
        }}
        else if(combo_tipo.getSelectedItem().equals("NoTripulada")){
            if(txtOrb.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Debe ingresar una orbita. EJ:1281299 km de la tierra", "ERROR", JOptionPane.ERROR_MESSAGE);
            return  false;
        }}
       
        return true;  
    
 }
 
    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        if(verificarDatos()){
        if(Fabrica.getInstancia().getInterfazNave().verificarNave(Integer.parseInt(txtCod.getText()))){
        javax.swing.JOptionPane.showMessageDialog(null, "Ya existe una nave con ese codigo!","ERROR!",0);
        }else{
        if(null != combo_tipo.getSelectedItem().toString()) // TODO add your handling code here:
        switch (combo_tipo.getSelectedItem().toString()) {
            case "Lanzadera":
                Lanzadera l = new Lanzadera();
                l.setActivo(1);
                l.setCodigo(Integer.parseInt(txtCod.getText()));
                l.setNombre(txtNom.getText());
                l.setPeso(Double.parseDouble(txtPeso.getText()));
                l.setCombustible(txtComb.getText());
                l.setEstado("En Base");
                l.setPropulsion(txtProp.getText());
                l.setTipo(combo_tipo.getSelectedItem().toString());
                conexionBD.getInstance().persist(l);
                JOptionPane.showMessageDialog(this, "Se ha creado la nave correctamente", "√âXITO", JOptionPane.INFORMATION_MESSAGE);
                break;
            case "No Tripulada":
                NoTripulado nt = new NoTripulado();
                nt.setActivo(1);
                nt.setCodigo(Integer.parseInt(txtCod.getText()));
                nt.setNombre(txtNom.getText());
                nt.setEstado("En Base");
                nt.setPeso(Double.parseDouble(txtPeso.getText()));
                nt.setCombustible(txtComb.getText());
                nt.setOrbita(txtOrb.getText());
                nt.setTipo(combo_tipo.getSelectedItem().toString());
                conexionBD.getInstance().persist(nt);
                JOptionPane.showMessageDialog(this, "Se ha creado la nave correctamente", "√âXITO", JOptionPane.INFORMATION_MESSAGE);
                break;
            case "Tripulada":
                Tripulado t = new Tripulado();
                t.setActivo(1);
                t.setCodigo(Integer.parseInt(txtCod.getText()));
                t.setNombre(txtNom.getText());
                t.setEstado("En Base");
                t.setPeso(Double.parseDouble(txtPeso.getText()));
                t.setCombustible(txtComb.getText());
                t.setOrbita(txtOrb.getText());
                t.setCant_pasajeros(Integer.parseInt(txtCantPasj.getText()));
                t.setTipo(combo_tipo.getSelectedItem().toString());
                conexionBD.getInstance().persist(t);
                JOptionPane.showMessageDialog(this, "Se ha creado la nave correctamente", "√âXITO", JOptionPane.INFORMATION_MESSAGE);
                break;
            default:
                break;
        }
          //Limpiar cajas de texto ---------------------------------------
            txtCod.setText("");
            txtNom.setText("");
            txtPeso.setText("");
            txtComb.setText(""); 
            txtOrb.setText("");
            txtProp.setText(""); 
            txtCantPasj.setText("");
        
    }
        }
    }//GEN-LAST:event_btn_guardarActionPerformed

