    private void btn_elimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_elimActionPerformed
        // TODO add your handling code here:
        int cont=0;
        List <Mision> misiones = pd.getMisiones();
        if (isEmpty(misiones)){
        conexionBD.getInstance().delete(pd);
        JOptionPane.showMessageDialog(this, "Se ha borrado el planeta correctamente", "ÉXITO", JOptionPane.INFORMATION_MESSAGE);
        }else{
        for (Mision m : misiones) {
            if(m.getEstado().equals("En curso")){
               cont++;
               }
        }
        if(cont>0){
            JOptionPane.showMessageDialog(this, "Primero debe eliminar o finalizar las misiones asignadas a esta nave", "ERROR", JOptionPane.ERROR_MESSAGE);
        }else{
            pd.setActivo(0);
            conexionBD.getInstance().merge(pd);
            JOptionPane.showMessageDialog(this, "Se ha dado de baja el planeta correctamente", "ÉXITO", JOptionPane.INFORMATION_MESSAGE);
        }
    
    }
        verPlanetas();
    }//GEN-LAST:event_btn_elimActionPerformed

