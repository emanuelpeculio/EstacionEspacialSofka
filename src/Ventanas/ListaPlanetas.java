            public final void verPlanetas() {
        DefaultTableModel modelo = (DefaultTableModel) jTablePlanetas.getModel();
        modelo.setRowCount(0);
        planetas = Fabrica.getInstancia().getInterfazPlaneta().getAllPlanetas();
        for (int i = 0; i < planetas.size(); i++) {
            PlanetaDestino p=(PlanetaDestino)planetas.get(i);
            Object[] dat = {p.getCodigo(),p.getDescripcion(),p.getNombre(),p.getTamaño()};
            modelo.addRow(dat);
        }
        DefaultTableCellRenderer Alinear = new DefaultTableCellRenderer();
        Alinear.setHorizontalAlignment(SwingConstants.CENTER);//.LEFT .RIGHT .CENTER
        jTablePlanetas.getColumnModel().getColumn(0).setCellRenderer(Alinear);
        jTablePlanetas.getColumnModel().getColumn(1).setCellRenderer(Alinear);
        jTablePlanetas.getColumnModel().getColumn(2).setCellRenderer(Alinear);
        jTablePlanetas.getColumnModel().getColumn(3).setCellRenderer(Alinear);


    }
    
